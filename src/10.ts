const completeLine = (l: string): string | undefined => {
    const stack: string[] = [];
    const isOk = l.split('').map((c) => {
        let ok = true;
        switch (c) {
            case '(':
                stack.push(c);
                break;
            case '[':
                stack.push(c);
                break;
            case '{':
                stack.push(c);
                break;
            case '<':
                stack.push(c);
                break;
            case ')':
                ok = stack.pop() === '(';
                break;
            case ']':
                ok = stack.pop() === '[';
                break;
            case '}':
                ok = stack.pop() === '{';
                break;
            case '>':
                ok = stack.pop() === '<';
                break;
        }
        return ok;
    }).every((v) => v);
    if (isOk && stack.length) {
        return stack.reverse().map((c) => {
            switch (c) {
                case '(':
                    return ')';
                case '[':
                    return ']';
                case '{':
                    return '}';
                case '<':
                    return '>';
            }
        }).join('');
    }
};

const scoreFun = (s: string): number => {
    let sc = 0;
    const tokens = '_)]}>';

    s.split('').forEach((c) => {
        sc *= 5;
        sc += tokens.indexOf(c);
    });

    return sc;
}


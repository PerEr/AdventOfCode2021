const evaluateLine = (l: string): number => {
    const stack: string[] = [];
    const errors = l.split('').map((c) => {
        let ok = true;
        switch (c) {
            case '(':
                stack.push(c);
                break;
            case '[':
                stack.push(c);
                break;
            case '{':
                stack.push(c);
                break;
            case '<':
                stack.push(c);
                break;
            case ')':
                ok = stack.pop() === '(';
                break;
            case ']':
                ok = stack.pop() === '[';
                break;
            case '}':
                ok = stack.pop() === '{';
                break;
            case '>':
                ok = stack.pop() === '<';
                break;
        }
        return !ok ? c : undefined;
   }).filter((v) => v);
   const first = errors.length > 0 ? errors[0] : ' ';
   switch (first) {
       case ')':
           return 3;
        case ']':
            return 57;
        case '}':
            return 1197;
        case '>':
            return 25137;
   }
   return 0;
};

type Token = '[' | ']' | ',';
type Symbol = Token | number;

const addTokens = (t1: Symbol[], t2: Symbol[]): Symbol[] => {
    return ['[', ...t1, ',', ...t2, ']'];
};

const explodeToken = (t: Symbol[]): Symbol[] | undefined => {
    const next: Symbol[] = [];
    let depth = 0;
    let lastNumIx = -1;
    let carryRight = -1;
    let copyAsIs = false;
    let modified = false;
    for (let ix = 0 ; ix<t.length ; ++ix) {
        let s = t[ix];
        if (copyAsIs) {
            // Nuthing
        } else if (s === ']') {
            --depth;
        } else if (depth === 5 && !modified) {
            const l = s as number;
            carryRight = t[ix+2] as number;
            ix += 3;
            s = 0;
            depth -= 1;
            next.pop();
            if (lastNumIx >= 0) {
                next[lastNumIx] = l + (next[lastNumIx] as number);
            }
            modified = true;
        } else if (typeof s === 'number') {
            if (carryRight >= 0) {
                s = carryRight + s;
                carryRight = -1;
                copyAsIs = true;
            }
            lastNumIx = next.length;
        } else if (s === '[') {
            ++depth;
        } 
        next.push(s);
    }
    return modified ? next : undefined;
}

const splitToken = (t: Symbol[]): Symbol[] | undefined => {
    const next: Symbol[] = [];
    let modified = false;

    for (let ix = 0 ; ix<t.length ; ++ix) {
        let s = t[ix];
        if (modified) {
            // Nuthing
        } else if (typeof s === 'number') {
            if (s >= 10) {
                next.push('[');
                next.push(Math.floor(s/2));
                next.push(',');
                next.push(Math.ceil(s/2));
                s = ']';
                modified = true;
            }
        }
        next.push(s);
    }

    return modified ? next : undefined;
}
const reduceToken = (t: Symbol[]): Symbol[] => {
    let syms: Symbol[] | undefined = t;
    let reply: typeof t;
    do {
        reply = syms;
        const tmp = explodeToken(syms);
        syms = !tmp ? splitToken(syms) : tmp;
    } while (syms);
    return reply;
}

const magnitude = (t: Symbol[]): number => {
    class SymbolStream { 
        t: Symbol[];
        offset: number;
        constructor(t: Symbol[]) { 
            this.t = t;
            this.offset = 0;
        }    
        isEmpty() {
            return this.offset >= this.t.length;
        }
        take() {
            return this.t[this.offset++];
        }
        peek() {
            return this.t[this.offset];
        }
     }
    
     type Pair = [Pair | number, Pair | number];

     const readPair = (d: number, ss: SymbolStream): Pair | undefined => {
        const start = ss.take();
        if (start === '[') {
            const left = typeof ss.peek() === 'number' ? ss.take() as number : readPair(d+1, ss);
            const comma = ss.take();
            if (comma === ',' && left != undefined) {
                const right = typeof ss.peek() === 'number' ? ss.take() as number : readPair(d+1,ss);
                const end = ss.take();
                if (end === ']' && right != undefined) {
                    return [left, right];
                }
            }
        }
    }
    const calcMagnitude = (p: Pair | number): number => {
        if (typeof p === 'number') {
            return p;
        } else {
            return 3 * calcMagnitude(p[0]) + 2 * calcMagnitude(p[1]);
        }
    };
    const ss = new SymbolStream(t);
    const p = readPair(0, ss);

    return p ? calcMagnitude(p) : 0;
}

const highestPairSum = ([head, ...tail]: Symbol[][]):number => {
    const r = tail.length > 1 ? highestPairSum(tail) : 0;
    const l = tail
        .map((v) => {
            return [addTokens(head, v), addTokens(v, head)]
                .map(reduceToken)
                .map(magnitude)
                .reduce((a, b) => Math.max(a, b), 0);
        })
        .reduce((a, b) => Math.max(a, b), 0);
    return Math.max(l, r);
}

